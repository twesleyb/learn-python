#!/usr/bin/env python3

''' Creating a Python virtual environment. '''

## There are several ways of creating a virtual environment in python.
# * anaconda/conda
# * python venv module
# * virtualenv

# To Create a Python3 virtual environment, with venv:
'''
# Create a new env:
python3 -m venv /path/to/env

# Activate it
source /path/to/env/bin/activate
'''


# Create a virtual environment with virtualenv:
'''
virtualenv path/to/env

source /path/to/env/bin/activate
'''
